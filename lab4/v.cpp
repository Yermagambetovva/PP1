#include <iostream>
using namespace std;

int main() {
    int n;
    cin >> n; // Вводим верхнюю границу диапазона

    // Перебираем все числа от 2 до n
    for (int i = 2; i <= n; i++) {
        bool isPrime = true; // Предполагаем, что число i простое

        // Проверяем, делится ли i на любое число от 2 до i-1
        for (int j = 2; j * j <= i; j++) {
            if (i % j == 0) { // Если число делится, оно не простое
                isPrime = false;
                break;
            }
        }

        // Если число осталось простым, выводим его
        if (isPrime) {
            cout << i << " is prime" << endl;
        }
    }

    return 0;
}

//Объяснение кода:
//1.	Вводится число n — верхняя граница диапазона.
//2.	Для каждого числа i от 2 до n проверяется, является ли оно простым:
//•	Внутренний цикл проверяет, делится ли i на любое число от 2 до корня из i.
//•	Если находится делитель, то число не простое.
//3.	Если число остаётся простым после всех проверок, оно выводится на экран.

