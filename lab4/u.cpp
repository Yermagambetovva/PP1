#include <iostream>
#include <iomanip> // для управления форматированием вывода
using namespace std;

int main() {
    int n;
    cin >> n; // Ввод размера матрицы

    int arr[100][100]; // Матрица 100x100 как максимум
    int sum = 0; // Для суммы чисел

    // Чтение элементов матрицы
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cin >> arr[i][j];
        }
    }

    // Обработка и вывод матрицы
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            // Проверка границ и диагоналей
            if (i == 0 || i == n - 1 || j == 0 || j == n - 1 || i == j || i + j == n - 1) {
                cout << setw(3) << "*"; // Выводим '' для границ и диагоналей
            } else {
                cout << setw(3) << arr[i][j]; // Выводим само число
                sum += arr[i][j]; // Добавляем это число к сумме
            }
        }
        cout << endl; // Переход на следующую строку
    }

    // Вывод суммы
    cout << sum << endl;
return 0;
}

//Объяснение кода:
//1.	Вводится размер матрицы.
//2.	Читается матрица размером n x n.
//3.	Программа проверяет, находится ли элемент на границе или диагонали. Если да, то на его место выводится *, иначе выводится сам элемент и добавляется к сумме.
//4.	После завершения вывода матрицы программа выводит сумму всех элементов, которые не находятся на границе или диагоналях.
//Этот код также использует setw(3) для форматирования вывода, чтобы каждый элемент занимал ровно 3 позиции.