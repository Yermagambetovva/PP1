#include <iostream>
using namespace std;
int main(){
    int n;
    cin >> n;

    //циклы для формирования таблицы умножения
    for(int i=0;i<n;i++){
        for(int j=0;j<n;j++){
            if(i == 0 || j == 0){
                cout << (i == 0 ? j : i) << " ";           //первая строка и первый столбец выводят индексы
            } else if ( i == j){
                cout << i*j << " ";     //диагональные элементы
            } else{
                cout << "0 ";            // все остальные эл 0
            }
        }
        cout << endl;
    }
    
    return 0;
}

//1.	Программа принимает размер таблицы n.
//2.	Если элемент находится в первой строке или первом столбце (i == 0 || j == 0), выводится его индекс.
//3.	Если элемент находится на диагонали (i == j), выводится произведение индексов.
//Исправления:
//1.	В выводе для всех элементов добавлен пробел после каждого числа (cout << "0 ") для корректного форматирования.
//2.	Теперь строки выводятся с нужными разделителями, и результат будет правильно выровнен.