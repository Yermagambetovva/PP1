//field[i][j] = mines_count > 0 ? mines_count + '0' : '.';
//Это выражение означает:
//•	Если mines_count > 0 (т.е. если вокруг клетки есть мины), то в ячейку записывается число (mines_count + '0' — это преобразование числа в символ).
//•	Если mines_count == 0 (вокруг клетки нет мин), то в ячейку записывается символ точки ('.').
//Таким образом, тернарный оператор в данном случае позволяет сократить запись, которая эквивалентна следующему коду:
