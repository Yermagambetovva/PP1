#include <iostream>
using namespace std;
int main(){
    int n, m, k;
    cin >> n >> m >> k;
    
    char field[1000][1000] = {};  
    // Координаты мин
    for (int i = 0; i < k; ++i) {
        int x, y;
        cin >> x >> y;
        field[x][y] = '*';
    }

    // Рассчет чисел вокруг мин
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < m; ++j) {
            if (field[i][j] == '*') continue;

            int mines_count = 0;
            for (int dx = -1; dx <= 1; ++dx) {
                for (int dy = -1; dy <= 1; ++dy) {
                    int ni = i + dx, nj = j + dy;
                    if (ni >= 0 && ni < n && nj >= 0 && nj < m && field[ni][nj] == '*') {
                        ++mines_count;
                    }
                }
            }
            field[i][j] = mines_count > 0 ? mines_count + '0' : '.';
        }
    }
    // Вывод поля
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < m; ++j) {
            if (field[i][j] == 0) field[i][j] = '.';  // пустые клетки заменяем на '.'
            cout << field[i][j] << ' ';
        }
        cout << endl;
    }

    return 0;
}

//Оператор ? : в C++ — это тернарный оператор, который является коротким условным выражением и заменяет конструкцию if-else.
//Общая форма тернарного оператора:
//условие ? выражение_если_истина : выражение_если_ложь;
//Он проверяет условие и в зависимости от его истинности выполняет одно из двух выражений.
