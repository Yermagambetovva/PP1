#include <iostream>
#include <vector>
using namespace std;
int main() {
    vector<int> frags;
    int input;

    // Чтение ввода до тех пор, пока не встретится 0
    while (true) {
        cin >> input;
        if (input == 0) {
            break;
        }
        frags.push_back(input);
    }

    int n = frags.size();
    vector<int> results;

    // Логика парования и суммирования значений
    for (int i = 0; i < n / 2; ++i) {
        results.push_back(frags[i] + frags[n - 1 - i]);
    }

    // Обработка случая, когда есть центральный элемент без пары
    if (n % 2 == 1) {
        results.push_back(frags[n / 2]); // Центральный элемент остается неизменным
    }

    // Печать результатов в одной строке
    for (size_t i = 0; i < results.size(); ++i) {
        cout << results[i];
        if (i < results.size() - 1) {
            cout << " "; // Добавляем пробел между числами
        }
    }
    cout << endl; // Переводим строку после вывода всех чисел

    return 0;
}





//Объяснение:
//Чтение ввода: Программа читает целые числа из стандартного ввода до тех пор, пока не встретит 0, который служит сигналом о завершении ввода.
//Использование вектора: Фраги хранятся в vector<int>.
//Логика парования: Программа суммирует пары, обращаясь к элементам от начала и конца вектора, двигаясь к центру.
//Обработка нечётного числа игроков: Если количество игроков нечётное, центральный элемент остаётся неизменным.
//Вывод: Результаты выводятся, показывая суммы фрагов для каждой пары или неизменённый центральный фраг.